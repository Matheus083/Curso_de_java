package aulas;

public class aula03_inferencia_de_tipos {

    public static void main(String[] args) {
        double a = 4.3;
        System.out.println(a);
        a = 12;

        var b = 4.1;
        System.out.println(b);

        var c = "TEXT.";
        System.out.println(c);
        c = "Other text.";

        double d;
        d = 1234.34;
        System.out.println(d);
    }
}
